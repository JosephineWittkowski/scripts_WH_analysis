  #print "x= ", x, ", y= ", y
  if y<(-x+210) : return 0
  elif y<(-x+290) :
    if y>(x-210):
      return 1
    else:
      return 2
  elif y<(-x+360) : 
    if y>(x-210):
      return 3
    elif y>(x-290):
      return 4
    else:
      return 5
  else : return 6
